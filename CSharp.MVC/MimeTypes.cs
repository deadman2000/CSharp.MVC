        private static char[] pathSeparators = new char[] { Path.DirectorySeparatorChar, Path.AltDirectorySeparatorChar, Path.VolumeSeparatorChar };
        private static string GetFileName(string path)
        {
            int startIndex = path.LastIndexOfAny(pathSeparators);
            if (startIndex < 0)
                return path;
            return path.Substring(startIndex);
        }

        public static string GetMimeMapping(string fileName)
        {
            fileName = GetFileName(fileName);
            for (int startIndex = 0; startIndex < fileName.Length; ++startIndex)
            {
                string str;
                if (fileName[startIndex] == '.' && _mimeTypes.TryGetValue(fileName.Substring(startIndex), out str))
                    return str;
            }
            return _mimeTypes[".*"];
        }
